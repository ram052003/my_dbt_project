QueryOut AS (
    /* transType: "Expression" */
    SELECT
        ACCOUNT_NUMBER AS ACCOUNT_NUMBER_CIS,
        CUSTOMER_NAME AS CUSTOMER_NAME_CIS,
        CUSTOMER_NUMBER AS CIS_NUMBER_CIS,
        TO_CHAR(TAXPAYER_ID_NUMBER) AS TAXID_NUMBER_CIS,
        CUSTOMER_NAME AS RELATED_NAME_CIS,
        RTRIM(IFF(CUSTOMER_CHARGE_CENTER_NUMBER = 5, 'YES', 'NO')) AS EMPLOYEE_FLAG_CIS,
        '' AS FT_BARCODE,
        CUSTOMER_CHARGE_CENTER_NUMBER
    FROM DSR_QueryOut
