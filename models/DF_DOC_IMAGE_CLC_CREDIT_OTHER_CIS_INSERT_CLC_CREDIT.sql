Table_Comparison_LKPOut AS (
    /* transType: "Lookup Procedure" */
    SELECT
        qnc.ACCOUNT_NUMBER AS ACCOUNT_NUMBER_1,
        qnc.CUSTOMER_NAME AS CUSTOMER_NAME_1,
        qnc.CIS_NUMBER AS CIS_NUMBER_1,
        qnc.TAXID_NUMBER AS TAXID_NUMBER_1,
        qnc.RELATED_NAME AS RELATED_NAME_1,
        qnc.EMPLOYEE_FLAG AS EMPLOYEE_FLAG_1,
        qnc.FT_BARCODE AS FT_BARCODE_1,
        qnc.ACCOUNT_BRANCH_CBC_NUMBER AS ACCOUNT_BRANCH_CBC_NUMBER_1,
        tgt.ACCOUNT_NUMBER AS ACCOUNT_NUMBER_2,
        tgt.CUSTOMER_NAME AS CUSTOMER_NAME_2,
        tgt.CIS_NUMBER AS CIS_NUMBER_2,
        tgt.TAXID_NUMBER AS TAXID_NUMBER_2,
        tgt.RELATED_NAME AS RELATED_NAME_2,
        tgt.EMPLOYEE_FLAG AS EMPLOYEE_FLAG_2,
        tgt.FT_BARCODE AS FT_BARCODE_2,
        tgt.ACCOUNT_BRANCH_CBC_NUMBER AS ACCOUNT_BRANCH_CBC_NUMBER_2
    FROM Query_Not_in_CLCOut AS qnc
    LEFT JOIN DBO.CLC_CREDITOut AS tgt
        ON qnc.CIS_NUMBER = tgt.CIS_NUMBER
       AND qnc.ACCOUNT_BRANCH_CBC_NUMBER = tgt.ACCOUNT_BRANCH_CBC_NUMBER
),

Table_Comparison_EXPOut AS (
    /* transType: "Expression" */
    SELECT
        ACCOUNT_NUMBER,
        CUSTOMER_NAME,
        CIS_NUMBER,
        TAXID_NUMBER,
        RELATED_NAME,
        EMPLOYEE_FLAG,
        FT_BARCODE,
        ACCOUNT_BRANCH_CBC_NUMBER,
        MD5(
            CIS_NUMBER || ACCOUNT_BRANCH_CBC_NUMBER || ACCOUNT_NUMBER ||
            CUSTOMER_NAME || TAXID_NUMBER || RELATED_NAME || EMPLOYEE_FLAG
        ) AS src_hashcode,
        MD5(
            CIS_NUMBER || ACCOUNT_BRANCH_CBC_NUMBER || ACCOUNT_NUMBER ||
            CUSTOMER_NAME || TAXID_NUMBER || RELATED_NAME || EMPLOYEE_FLAG
        ) AS tgt_hashcode,
        CASE
            WHEN CIS_NUMBER IS NULL AND ACCOUNT_BRANCH_CBC_NUMBER IS NULL THEN 1
            WHEN src_hashcode != tgt_hashcode THEN 3
            ELSE 0
        END AS change_code
    FROM Table_Comparison_LKPOut
),

