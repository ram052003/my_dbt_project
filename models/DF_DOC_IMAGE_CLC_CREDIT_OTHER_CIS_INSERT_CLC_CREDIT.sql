Table_Comparison_LKPOut AS (
    /* transType: "Lookup Procedure" */
    SELECT
        q.ACCOUNT_NUMBER AS ACCOUNT_NUMBER_1,
        q.CUSTOMER_NAME AS CUSTOMER_NAME_1,
        q.CIS_NUMBER AS CIS_NUMBER_1,
        q.TAXID_NUMBER AS TAXID_NUMBER_1,
        q.RELATED_NAME AS RELATED_NAME_1,
        q.EMPLOYEE_FLAG AS EMPLOYEE_FLAG_1,
        q.FT_BARCODE AS FT_BARCODE_1,
        q.ACCOUNT_BRANCH_CBC_NUMBER AS ACCOUNT_BRANCH_CBC_NUMBER_1,
        c.ACCOUNT_NUMBER AS ACCOUNT_NUMBER_2,
        c.CUSTOMER_NAME AS CUSTOMER_NAME_2,
        c.CIS_NUMBER AS CIS_NUMBER_2,
        c.TAXID_NUMBER AS TAXID_NUMBER_2,
        c.RELATED_NAME AS RELATED_NAME_2,
        c.EMPLOYEE_FLAG AS EMPLOYEE_FLAG_2,
        c.FT_BARCODE AS FT_BARCODE_2,
        c.ACCOUNT_BRANCH_CBC_NUMBER AS ACCOUNT_BRANCH_CBC_NUMBER_2
    FROM Query_Not_in_CLCOut AS q
    LEFT JOIN {{ ref('CLC_CREDIT') }} AS c
        ON q.CIS_NUMBER = c.CIS_NUMBER
       AND q.ACCOUNT_BRANCH_CBC_NUMBER = c.ACCOUNT_BRANCH_CBC_NUMBER
),

Table_Comparison_EXPOut AS (
    /* transType: "Expression" */
    SELECT
        ACCOUNT_NUMBER_1 AS ACCOUNT_NUMBER,
        CUSTOMER_NAME_1 AS CUSTOMER_NAME,
        CIS_NUMBER_1 AS CIS_NUMBER,
        TAXID_NUMBER_1 AS TAXID_NUMBER,
        RELATED_NAME_1 AS RELATED_NAME,
        EMPLOYEE_FLAG_1 AS EMPLOYEE_FLAG,
        FT_BARCODE_1 AS FT_BARCODE,
        ACCOUNT_BRANCH_CBC_NUMBER_1 AS ACCOUNT_BRANCH_CBC_NUMBER,
        MD5(
            CONCAT(
                TO_VARCHAR(CIS_NUMBER_1),
                TO_VARCHAR(ACCOUNT_BRANCH_CBC_NUMBER_1),
                TO_VARCHAR(ACCOUNT_NUMBER_1),
                TO_VARCHAR(CUSTOMER_NAME_1),
                TO_VARCHAR(TAXID_NUMBER_1),
                TO_VARCHAR(RELATED_NAME_1),
                TO_VARCHAR(EMPLOYEE_FLAG_1)
            )
        ) AS src_hashcode,
        MD5(
            CONCAT(
                TO_VARCHAR(CIS_NUMBER_2),
                TO_VARCHAR(ACCOUNT_BRANCH_CBC_NUMBER_2),
                TO_VARCHAR(ACCOUNT_NUMBER_2),
                TO_VARCHAR(CUSTOMER_NAME_2),
                TO_VARCHAR(TAXID_NUMBER_2),
                TO_VARCHAR(RELATED_NAME_2),
                TO_VARCHAR(EMPLOYEE_FLAG_2)
            )
        ) AS tgt_hashcode,
        CASE
            WHEN CIS_NUMBER_1 IS NULL AND ACCOUNT_BRANCH_CBC_NUMBER_1 IS NULL THEN 1
            WHEN src_hashcode != tgt_hashcode THEN 3
            ELSE 0
        END AS change_code
    FROM Table_Comparison_LKPOut
),

Map_InsertOut AS (
    /* transType: "Router" */
    SELECT
        ACCOUNT_NUMBER,
        CUSTOMER_NAME,
        CIS_NUMBER,
        TAXID_NUMBER,
        RELATED_NAME,
        EMPLOYEE_FLAG,
        FT_BARCODE,
        ACCOUNT_BRANCH_CBC_NUMBER,
        src_hashcode,
        tgt_hashcode,
        change_code
    FROM Table_Comparison_EXPOut
    WHERE change_code = 1
)

