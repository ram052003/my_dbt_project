        T.FT_BARCODE AS FT_BARCODE_2,
        T.ACCOUNT_BRANCH_CBC_NUMBER AS ACCOUNT_BRANCH_CBC_NUMBER_2
    FROM Query_2ndOut Q
    LEFT JOIN DBO.TREASURY_CREDITOut T
        ON Q.ACCOUNT_NUMBER = T.ACCOUNT_NUMBER
        AND Q.NOTE_NUMBER = T.NOTE_NUMBER
        AND Q.COMMITMENT_NUMBER = T.COMMITMENT_NUMBER
        AND Q.CIS_NUMBER = T.CIS_NUMBER
),

Table_Comparison_EXPOut AS (
    /* transType: "Expression" */
    SELECT
        ACCOUNT_NUMBER,
        NOTE_NUMBER,
        COMMITMENT_NUMBER,
        CUSTOMER_NAME,
        CIS_NUMBER,
        TAXID_NUMBER,
        RELATED_NAME,
        RELATED_ACCOUNT,
        STATUS,
        EMPLOYEE_FLAG,
        FT_BARCODE,
        ACCOUNT_BRANCH_CBC_NUMBER,
        MD5(
            CONCAT(
                ACCOUNT_NUMBER,
                NOTE_NUMBER,
                COMMITMENT_NUMBER,
                CIS_NUMBER,
                CUSTOMER_NAME,
                TAXID_NUMBER,
                STATUS
            )
        ) AS src_hashcode,
        MD5(
            CONCAT(
                ACCOUNT_NUMBER,
                NOTE_NUMBER,
                COMMITMENT_NUMBER,
                CIS_NUMBER,
                CUSTOMER_NAME,
                TAXID_NUMBER,
                STATUS
            )
        ) AS tgt_hashcode,
        CASE
            WHEN ACCOUNT_NUMBER IS NULL
                 AND NOTE_NUMBER IS NULL
                 AND COMMITMENT_NUMBER IS NULL
                 AND CIS_NUMBER IS NULL
            THEN 1
            WHEN src_hashcode != tgt_hashcode THEN 3
            ELSE 0
        END AS change_code
    FROM Table_Comparison_LKPOut
),

