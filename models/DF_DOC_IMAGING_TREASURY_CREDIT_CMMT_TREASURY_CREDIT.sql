        Q.STATUS AS STATUS_1,
        Q.EMPLOYEE_FLAG AS EMPLOYEE_FLAG_1,
        Q.FT_BARCODE AS FT_BARCODE_1,
        Q.ACCOUNT_BRANCH_CBC_NUMBER AS ACCOUNT_BRANCH_CBC_NUMBER_1,
        T.ACCOUNT_NUMBER AS ACCOUNT_NUMBER_2,
        T.NOTE_NUMBER AS NOTE_NUMBER_2,
        T.COMMITMENT_NUMBER AS COMMITMENT_NUMBER_2,
        T.CUSTOMER_NAME AS CUSTOMER_NAME_2,
        T.CIS_NUMBER AS CIS_NUMBER_2,
        T.TAXID_NUMBER AS TAXID_NUMBER_2,
        T.RELATED_NAME AS RELATED_NAME_2,
        T.RELATED_ACCOUNT AS RELATED_ACCOUNT_2,
        T.STATUS AS STATUS_2,
