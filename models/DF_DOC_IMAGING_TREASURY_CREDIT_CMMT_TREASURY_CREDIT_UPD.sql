    /* transType: "Expression" */
    select
        ACCOUNT_NUMBER,
        NOTE_NUMBER,
        COMMITMENT_NUMBER,
        CUSTOMER_NAME,
        CIS_NUMBER,
        TAXID_NUMBER,
        RELATED_NAME,
        RELATED_ACCOUNT,
        STATUS,
        EMPLOYEE_FLAG,
        FT_BARCODE,
        ACCOUNT_BRANCH_CBC_NUMBER,
        md5( concat(ACCOUNT_NUMBER, NOTE_NUMBER, COMMITMENT_NUMBER, CIS_NUMBER, CUSTOMER_NAME, TAXID_NUMBER, STATUS) ) as src_hashcode,
        md5( concat(ACCOUNT_NUMBER, NOTE_NUMBER, COMMITMENT_NUMBER, CIS_NUMBER, CUSTOMER_NAME, TAXID_NUMBER, STATUS) ) as tgt_hashcode,
        case
            when ACCOUNT_NUMBER is null
                 and NOTE_NUMBER is null
                 and COMMITMENT_NUMBER is null
                 and CIS_NUMBER is null then 1
            when src_hashcode <> tgt_hashcode then 3
            else 0
        end as change_code
    from Table_Comparison_LKPOut
