            SELECT
                ID,
                BATCH_ID,
                TARGET_TABLE_NAME,
                FILE_NAME,
                ROW_NUMBER() OVER (
                    PARTITION BY BATCH_ID, TARGET_TABLE_NAME, FILE_NAME
                    ORDER BY rnk_fst DESC NULLS LAST
                ) AS rnk_lst
            FROM (
                SELECT
                    ID,
                    BATCH_ID,
                    TARGET_TABLE_NAME,
                    FILE_NAME,
                    ROW_NUMBER() OVER (
                        PARTITION BY BATCH_ID, TARGET_TABLE_NAME, FILE_NAME
                        ORDER BY BATCH_ID, TARGET_TABLE_NAME, FILE_NAME NULLS LAST
                    ) AS rnk_fst
                FROM (
                    SELECT
                        BATCH_DETAILS.ID AS ID,
                        BATCH_DETAILS.BATCH_ID AS BATCH_ID,
                        BATCH_DETAILS.TARGET_TABLE_NAME AS TARGET_TABLE_NAME,
                        BATCH_DETAILS.FILE_NAME AS FILE_NAME,
                        BATCH_DETAILS.STATUS AS STATUS
                    FROM (
                        SELECT
                            id,
                            TARGET_TABLE_NAME,
                            BATCH_ID,
                            STATUS,
                            ACTIVE_FLAG,
                            BATCH_DETAILS_START_TIME,
                            FILE_NAME,
                            RANK() OVER (
                                PARTITION BY batch_id, TARGET_TABLE_NAME, FILE_NAME
                                ORDER BY BATCH_DETAILS_START_TIME DESC
                            ) AS RANK
